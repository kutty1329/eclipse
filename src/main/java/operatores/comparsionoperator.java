package operatores;

public class comparsionoperator {

	public static void main(String[] args) {
		// <,>,<=,>=,==------its called compartion operator
		
		double a =75.5d;
		float b = 25.5f;
		boolean c = a<b;
		boolean d = a>b;
		boolean e = a<=b;
		boolean f = a>=b;
		boolean g = a==b;
		
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);

	}

}
