package operatores;

public class assignmentoperatore {

	public static void main(String[] args) {
	
		//+=,-=,*=,/=,%=---- its called assignment operator
		
		int a = 35;
		int b = 10;
		int c = 20;
		int d = 45;
		int e = 10;
		a+=10;
		b-=5;
		c*=5;
		d/=5;
		e%=2;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		
		
				

	}

}
